    private void exportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exportActionPerformed
        //call CSV writing method here        

        filePath = this.fileName.getText();
        //construct a FileExport object using data from GUI
        FileExport f = new FileExport(
                teamNumbers,
                matchNumbers,
                autoSwitchCubes,
                autoScaleCubes,
                switchReaches,
                scaleReaches,
                baselineReaches,
                teleSwitchCubes,
                teleScaleCubes,
                teleExchCubes,
                climbs,
                platforms,
                allData
        );
        //consolidate data
        allData = f.get2DArr();        
        
        
        try
        {
            f.getIntCSV(allData,filePath); //make sure filepath has 2 \\ for between folders
        }
        catch (Exception ex) {
            System.out.println("Error");
        }
               
        //======================================================================
        //after data is exported, clear all of the arraylists so they don't
        //appear again in the csv file
        teamNumbers.clear();
        matchNumbers.clear();
        autoSwitchCubes.clear();
        autoScaleCubes.clear();
        switchReaches.clear();
        scaleReaches.clear();
        baselineReaches.clear();
        teleSwitchCubes.clear();
        teleScaleCubes.clear();
        teleExchCubes.clear();
        climbs.clear();
        platforms.clear();
    }//GEN-LAST:event_exportActionPerformed
  
    
