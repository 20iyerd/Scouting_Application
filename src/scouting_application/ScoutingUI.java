    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        //===============================GENERIC GUI HANDLING==============================
        matchNumbers.add(Integer.parseInt(this.matchNum.getText()));        
        teamNumbers.add(Integer.parseInt(this.teamNum.getText()));
        
        //===============================AUTONOMOUS GUI HANDLING==============================               
        autoSwitchCubes.add(autoSwitchCubesPlaced.getValue());
        autoScaleCubes.add(autoScaleCubesPlaced.getValue());
        
        switchReaches.add(reachedSwitch);
        scaleReaches.add(reachedScale);
        baselineReaches.add(crossBaseline);
        //====================================================================================

        //==============================TELEOP GUI HANDLING===================================
        teleSwitchCubes.add(teleSwitchCubesPlaced.getValue());
        teleScaleCubes.add(teleScaleCubesPlaced.getValue());
        teleExchCubes.add(teleExchCubesPlaced.getValue());
        
        climbs.add(climbedScale);
        platforms.add(climbedPlatform);
        //====================================================================================
                
        //========resetting values to default code=============
        this.matchNum.setText(null);
        this.teamNum.setText(null);
        
        autoBaseline.setSelected(false);
        autoSwitch.setSelected(false);
        autoScale.setSelected(false);
        platform.setSelected(false);
        climb.setSelected(false);
        
        autoSwitchCubesPlaced.setValue(0);
        autoScaleCubesPlaced.setValue(0);
        teleSwitchCubesPlaced.setValue(0);
        teleScaleCubesPlaced.setValue(0);
        teleExchCubesPlaced.setValue(0);
        
        reachedSwitch = false;
        reachedScale = false;
        crossBaseline = false;
        
        climbedPlatform = false;
        climbedScale = false;
        //=====================================================
    }//GEN-LAST:event_submitActionPerformed

